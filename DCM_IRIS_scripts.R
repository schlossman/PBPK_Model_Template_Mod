                    model.param.sheetname = "IRIS_model_rat_VarC", 
                    exposure.param.filename = "DCM_template_parameters_Exposure.xlsx", 
                    exposure.param.sheetname = "Oral_Periodic_IRIS_T5.1",
                    adj.parms = c(dose_water = daily.intake.m[ii]),
