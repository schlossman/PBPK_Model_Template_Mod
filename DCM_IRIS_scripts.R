lastweekavg <- function(res){
  # res should be n x 2 array: first column = time in days, 2nd column = metric to average 
  n <- dim(res)[1]
  n7 <- which(res[,1]==(res[n,1]-7))
  return((res[n,2] - res[n7,2])/7)
}

